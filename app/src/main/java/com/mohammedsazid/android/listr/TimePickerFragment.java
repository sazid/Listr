package com.mohammedsazid.android.listr;

import android.annotation.SuppressLint;
import android.app.Dialog;
import android.app.TimePickerDialog;
import android.os.Bundle;
import android.os.Handler;
import android.os.Message;
import android.support.v4.app.DialogFragment;
import android.text.format.DateFormat;
import android.widget.TimePicker;

import java.util.Calendar;

public class TimePickerFragment extends DialogFragment implements TimePickerDialog.OnTimeSetListener {

    private int timeHour;
    private int timeMinute;
    private Handler handler;

    @SuppressLint("ValidFragment")
    public TimePickerFragment(Handler handler) {
        this.handler = handler;
    }

    @Override
    public Dialog onCreateDialog(Bundle savedInstanceState) {
        // Use the current time as the default values for the picker
        final Calendar c = Calendar.getInstance();
        int hour = timeHour = c.get(Calendar.HOUR_OF_DAY);
        int minute = timeMinute = c.get(Calendar.MINUTE);

        // Create a new instance of TimePickerDialog and return it
        return new TimePickerDialog(getActivity(), this, hour, minute,
                DateFormat.is24HourFormat(getActivity()));
    }

    @Override
    public void onTimeSet(TimePicker view, int hourOfDay, int minute) {
        Bundle bundle = new Bundle();

        bundle.putInt("time_hour", hourOfDay);
        bundle.putInt("time_minute", minute);
        Message message = new Message();
        message.setData(bundle);
        handler.sendMessage(message);
    }
}
